def escreva(msg):
    tamanho = len(msg) + 4
    print('~' * tamanho)
    print(f'  {msg}')
    print('~' * tamanho)


escreva('Criando uma Função de mensagem adaptável')
escreva('Curso de Python do Gustavo Guanabara')
escreva('Estou frustrado mas eu estou adorando')
escreva('Quanto mais eu estudo menos eu sei')